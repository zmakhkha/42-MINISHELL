#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	size;
	int		i;

	size = ft_strlen(s);
	i = size;
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)&s[i]);
		i--;
	}
	return (0);
}
